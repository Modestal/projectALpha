                <h2>What is Artificial Intelligence?</h2>
                <p class="def"><em>Artificial Intelligence is a discipline that focus of developing 
                machines that like humans, will be capable of showing intuition and 
                intelligent behavior.</em></p>
                <p>Historically, the process of thinking has been very complex: one that 
                requires a great deal of analysis and deductions taking into cognizance, 
                ethical standards, situational variables, past experiences and a host of 
                other things&#8212;this is exactly what scientists are trying to initiate
                in machines.
                </p>
                <p>The field of artificial intelligence is a rather broad discipline 
                that comprise of machine learning, deep learning and neural network. 
                The overall advancement in artificial intelligence largely 
                depends on work done by scientists in these three sub-areas&#8212;especially neural network.
                </p>
                <p><strong>Machine Learning: </strong>Machine Learning is a branch of 
                artificial intelligence(AI) that provides machine with the ability to 
                to automatically learn and improve from past experiences without been 
                explicitly reprogrammed. The computers are trained by exposing them 
                to data through observations or instructions from which they are expected 
                discern patterns that will aid them to reach meaningful conclusions.
            </p>
            <p>As the computer is deployed, it constantly improve itself by 
            learning from the data it is exposed to, enhancing its abilities by 
            imbibing new data which it compares to the ones it was trained on. Machine learning ensures that 
            machines are able to accomplish other tasks different from those they 
            were initially trained to perform.
            </p>
            <p>The method by which machine learns could be: supervised, unsupervised and 
                reinforced
            </p>
            <p><span>Supervised Learning: </span>  The learning algorithm
            is presented with labeled examples from which it forms 
            models that enables it to make future predictions. In the process of 
            supervised learning, the learning algorithm takes a known sets of input 
            data and known responses to the data and trains a model to generate 
            responsible predictions for the response to new data.
            </p>
            <p><span>Unsupervised Learning: </span>The learning algorithm
            does not make use of labeled data, rather it seeks to find hidden 
            patterns in the data provided.  When patterns are identified,
            models will be generated which can be improved upon based on future data.
            </p>
            <p><span>reinforcement Learning: </span>Here learning
            algorithm interacts with their environment, learning by trial and error 
            and are rewarded  for correct actions. This of kind machine learning is used 
            to power the self driving car.
            </p>
            <p><strong>Deep Learning: </strong> Deep learning
            can be viewed as 
            a subfield of machine learning, but its core process is inspired by the 
            structure and function of the human brain known  as artificial neural 
            networks. Success in deep learning depend on the availability of huge 
            amount of data&#8212;which can be used to train the neural networks, and since 
            data is readily available in real-time, learning algorithm will continue 
            to get better, opening doors to unlimited possibilities in the field of 
            artificial intelligent.
        </p>
