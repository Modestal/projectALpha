            <h2>Features of Blockchain</h2>
            <p><strong>Distributed Ledger: </strong>Blockchain has no central authority, rather
            it is a distributed ledger working on the principle of a peer-to-peer network. The 
            information on the ledger are synchronized and spread across the nodes that make up the 
            network. Each nodes have a copy of all the transactions on the Bitcoin blockchain since inception. </p>
            <p><strong>Encryption: </strong> Transactions on the blockchain network are cryptographically
            hashed and secure with the SHA-256 for the Bitcoin blockchain. Before new blocks are added 
            to the blockchain, they are secured, and validated using hash algorithm making it difficult to counterfeit.</p>
            <p><strong>Proof of work: </strong>Blockchain uses proof of work to add new blocks 
            to the network. It is essentially a process in which miners compete with one 
            other to solve a complex computational puzzle. This becomes necessary when a transaction
             is announced and miners called to action&#8212;a process that leads to the creation new bitcon currency.
          </p>
          <p>In the proof of work, the miner that first solve the mathematical puzzle by finding the 
            correct nounce announce it to other miners, then they verifies the claim, he is 
            rewarded with 12.5 bitcoin and the transaction is added to the network.</p>
            <p> Finding the correct nounce is a very difficult process, which consumes a lot of
            computational resources in the form of electricity. But it is very
            easy to verify by other miners.
         </p>
           <p><strong>Immutability: </strong> Transactions, once added to the ledger cannot be altered. 
            Passing transactions through SHA-256 algorithm ensures that they are digitally signed and secured&#8212;
            making it virtually impossible to alter.</p> 
            <p> Remember the story of the dying man 
            who left his will in the custody of his lawyer right?&#8212; we all know that the lawyer 
            could easily alter the content of the will and no one may find out.
           But if the will were to be stored on a blockchain network, then it will be immutable, as the
           document will be cryptographically hashed and made public to the entire network. 
           Any attempt to alter, everyone in the network will know, and they will remove 
           it from the network.
          </p>
