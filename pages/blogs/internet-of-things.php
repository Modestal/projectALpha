        <p>Internet-of-things (IoT) is one  the new waves of technological 
        innovations sweeping across the tech industry at the moment. IoT&#8212; a term
        nicely coined by Kelvin Ashon in 1999 is used to describe a web of 
        interconnected devices communicating with one another over the cloud.
        </p>
        <p>IoT is an evolution of mobile, home and embedded applications that 
        are connected to the Internet, integrating greater computing capabilities
         and using data analytics to extract useful information. IoT is becoming
        increasingly popular with focus on process automation, creating an ever growing   
         intelligent systems from millions of connected devices interacting 
         with one other exchanging and analyzing data about both their status and 
         environment.
        </p>
        <p>The processes involved in the IoT ecosystem seems quite verbose, 
        but the principle is simple enough to get a glimpse of the underlying concept: 
        any thing that has an on and off switch will be connected to the Internet&#8212;that 
        is connecting everyday objects to the Internet; with the help of sensor and 
        wireless network, Just about any physical 
        object can be connected to the Internet.
        </p>
        <p> These connected devices interacts with each other analyzing data 
        collected and putting 
        them into meaningful uses in near-real-time, thereby automating our lives and interactions 
        in incredible ways. 
        </p>
        <h5>How does IoT work</h5>
        <p>IoT devices are connected via wi-fi, bluethoot or satellite and 
         communicate with each other forming an ever expanding intelligent 
         system that share data over the cloud: as data 
        is constantly been generated, from various connected devices, they are 
        captured by sensors and sent to the cloud where they are integrated and analyzed.
        The output or information is then swiftly channeled to areas and systems 
        where they are harnessed
        for process automation allowing for a truly optimized experiences. </p>
        <p>It is important to point out that getting devices from various 
        vendors to communicate with each other in an IoT-enabled settings would be very 
        challenging, as some of these gadget are written in completely different languages. 
        However, to go around these bottleneck, IoT providers have frameworks like
        Homekit from  Apple, Alexa from Amazon and Smarthings from Samsung&#8212;these 
        frameworks establish the communication between the plethora of IoT devices
        ensuring that they are seamlessly controlled remotely by users.
        </p>
